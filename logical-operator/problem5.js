let day = "mon";

if (day == "sun"){
  console.log("sunday");
}
else if (day == "mon"){
  console.log("monday");
}
else if (day == "tue"){
  console.log("tueday");
}
else if (day == "wed"){
  console.log("wedday");
}
else if (day == "thu"){
  console.log("thursday");
}
else if (day == "fri"){
  console.log("friday");
}
else if (day == "sat"){
  console.log("satday");
}else{
  console.log("invalid entry")
}